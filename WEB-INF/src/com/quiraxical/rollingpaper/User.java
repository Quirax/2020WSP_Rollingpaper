package com.quiraxical.rollingpaper;

public class User {
    private String name;
    private String nick;

    public void setName(String val) {
        this.name = val;
    }
    public String getName() {
        return this.name;
    }

    public void setNick(String val) {
        this.nick = val;
    }
    public String getNick() {
        return this.nick;
    }
}
